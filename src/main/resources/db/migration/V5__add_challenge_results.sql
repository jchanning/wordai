CREATE TABLE IF NOT EXISTS challenge_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    challenge_id VARCHAR(36) NOT NULL UNIQUE,
    user_id BIGINT NOT NULL,
    username_snapshot VARCHAR(100) NOT NULL,
    dictionary_id VARCHAR(50) NOT NULL,
    total_score INT NOT NULL,
    puzzles_completed INT NOT NULL,
    status VARCHAR(30) NOT NULL,
    completed_at TIMESTAMP NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_challenge_results_total_score_completed_at
    ON challenge_results (total_score DESC, completed_at ASC);
CREATE INDEX IF NOT EXISTS idx_challenge_results_user_id ON challenge_results (user_id);
